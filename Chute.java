package trabalhoamanda;

public class Chute {
private int forca;
private int quadrante;
private int x;
private int y;
private int defesa;
private int situação;


public int getSituação() {
	return situação;
}
public void setSituação(int situação) {
	this.situação = situação;
}
public int getDefesa() {
	return defesa;
}
public void setDefesa(int defesa) {
	this.defesa = defesa;
}
public int getForca() {
	return forca;
}
public void setForca(int forca) {
	this.forca = forca;
}
public int getQuadrante() {
	return quadrante;
}
public void setQuadrante(int quadrante) {
	this.quadrante = quadrante;
}
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}

}
